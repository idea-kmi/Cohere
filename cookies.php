	<h2>Cookies explained</h2>
	<p>A cookie is a small amount of data, which often includes a unique
	identifier that is sent to your computer or mobile phone browser from a
	website's computer and is stored on your computer’s or mobile phone’s
	hard drive. Each website can send its own cookie to your browser if your
	 browser's preferences allow it, which the site can then access when you
	 visit it again to track online traffic flows, for example. A website
	cannot access cookies sent by other websites.</p>

	<h2>How we use cookies</h2>
	<p>We use cookies to make sure our website work effectively and to
	improve your user experiences on them. Information supplied by cookies
	can help us work out how you use our site; for example, which pages you
	 look at. We also use cookies to understand how you interact with our
	site.</p>

	<h2>Types of cookie</h2>
	<p>Three types of cookie may be used during your visit to our website:</p>
	<ul>
		<li><strong>Session or temporary cookies</strong> that are deleted
	after each visit.</li>
		<li><strong>Persistent cookies</strong> that are valid across visits.
		They help our website remember you as a user each time you use the same computer to revisit us.</li>
		<li><strong>Third-party cookies</strong> that used by external content from other third-party
	websites within our website.</li>
	</ul>


